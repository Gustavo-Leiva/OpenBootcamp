//Sentencia switch

let nota = 5;

switch (nota) {
  case 5:
    console.log("sobresaliente");
    break;
  case 4:
    console.log("superado");
    break;
  case 3:
    console.log("aprobado");
    break;
  case 2:
    console.log("desaprobado");
    break;
  case 1:
    console.log("insuficiente");
    break;
  default:
    console.log("error");
    break;
}
